# ── load_and_normalise ────────────────────────────────────────────────────────
def load_and_normalise(cfg):
    print(f"\n  📂 Loading {cfg['name']}...")
    raw = load_dataset(cfg["hf_name"], cfg["hf_config"])
    frames = [raw[s].to_pandas() for s in cfg["splits"] if s in raw]
    if not frames:
        raise ValueError(f"No valid splits for {cfg['name']}")
    df = pd.concat(frames, ignore_index=True)
    print(f"     Columns available: {list(df.columns)}")

    # Build text
    text_parts = [df[c].fillna("").astype(str)
                  for c in cfg["text_cols"] if c in df.columns]
    if not text_parts:
        raise ValueError(f"No text columns found for {cfg['name']}")
    df["text"] = text_parts[0] if len(text_parts) == 1 else \
                 text_parts[0].str.cat(text_parts[1:], sep=" ")
    df["text"] = df["text"].str[:3000]

    # Filter to specific label values before binarising
    if "filter_labels" in cfg:
        df = df[df[cfg["label_col"]].isin(cfg["filter_labels"])].copy()
        print(f"     After label filter {cfg['filter_labels']}: {len(df):,} rows")

    # Binarise label
    df["label"] = (df[cfg["label_col"]] == cfg["spam_value"]).astype(int)
    df = df[["text", "label"]].dropna().reset_index(drop=True)

    # Sample if dataset is too large for RAM
    if "max_rows" in cfg and len(df) > cfg["max_rows"]:
        df = df.groupby("label", group_keys=False).apply(
            lambda x: x.sample(min(len(x), cfg["max_rows"] // 2),
                                random_state=SEED)
        ).reset_index(drop=True)
        print(f"     Sampled to {len(df):,} rows (balanced)")

    n_spam = df["label"].sum(); n_ham = len(df) - n_spam
    print(f"     Total: {len(df):,} | Spam: {n_spam:,} | Ham: {n_ham:,}")
    if n_spam < 100 or n_ham < 100:
        raise ValueError(f"Too few examples: spam={n_spam}, ham={n_ham}")
    return df

# ── EmailDataset ──────────────────────────────────────────────────────────────
class EmailDataset(Dataset):
    def __init__(self, texts, labels, tokenizer, max_length):
        self.encodings = tokenizer(list(texts), truncation=True,
                                   padding=True, max_length=max_length,
                                   return_tensors="pt")
        self.labels = torch.tensor(list(labels), dtype=torch.long)
    def __len__(self): return len(self.labels)
    def __getitem__(self, idx):
        return {"input_ids":      self.encodings["input_ids"][idx],
                "attention_mask": self.encodings["attention_mask"][idx],
                "labels":         self.labels[idx]}

# ── Training ──────────────────────────────────────────────────────────────────
def compute_metrics(eval_pred):
    logits, labels = eval_pred
    preds = np.argmax(logits, axis=1)
    return {"accuracy": accuracy_score(labels, preds),
            "f1": f1_score(labels, preds, zero_division=0)}

def train_model(train_dataset, eval_dataset, model_dir, tokenizer):
    model = AutoModelForSequenceClassification.from_pretrained(
        MODEL_NAME, num_labels=2)
    args = TrainingArguments(
        output_dir=str(model_dir / "checkpoints"),
        num_train_epochs=3,
        per_device_train_batch_size=32,
        per_device_eval_batch_size=64,
        eval_strategy="epoch",
        save_strategy="steps", save_steps=500, save_total_limit=2,
        load_best_model_at_end=False,
        learning_rate=2e-5, weight_decay=0.01, warmup_steps=500,
        logging_steps=100, seed=SEED,
        fp16=torch.cuda.is_available(),
        report_to="none", dataloader_num_workers=2,
    )
    trainer = Trainer(model=model, args=args,
                      train_dataset=train_dataset,
                      eval_dataset=eval_dataset,
                      processing_class=tokenizer,
                      compute_metrics=compute_metrics)
    trainer.train()
    trainer.save_model(str(model_dir))
    tokenizer.save_pretrained(str(model_dir))
    return model

# ── Inference ─────────────────────────────────────────────────────────────────
device = torch.device("cuda" if torch.cuda.is_available() else "cpu")

def predict(model, tokenizer, texts, batch_size=64):
    model.eval()
    all_preds = []
    for i in range(0, len(texts), batch_size):
        batch = list(texts[i:i+batch_size])
        enc = tokenizer(batch, truncation=True, padding=True,
                        max_length=MAX_LENGTH, return_tensors="pt")
        enc = {k: v.to(device) for k, v in enc.items()}
        with torch.no_grad():
            logits = model(**enc).logits
        all_preds.extend(torch.argmax(logits, dim=1).cpu().numpy())
    return np.array(all_preds)

def evaluate(model, tokenizer, texts, labels):
    preds = predict(model, tokenizer, texts)
    labels = np.array(labels)
    return {
        "accuracy":  accuracy_score(labels, preds),
        "precision": precision_score(labels, preds, zero_division=0),
        "recall":    recall_score(labels, preds, zero_division=0),
        "f1":        f1_score(labels, preds, zero_division=0),
        "cm":        confusion_matrix(labels, preds).tolist(),
        "preds":     preds,
    }

# ── Attack functions ──────────────────────────────────────────────────────────
HOMOGLYPH_MAP = {
    'a':'а','e':'е','o':'о','p':'р','c':'с',
    'x':'х','i':'і','u':'υ','y':'у','k':'к',
}

def apply_homoglyph(text, rate, rng=None):
    if rng is None: rng = random.Random(SEED)
    chars = list(text)
    eligible = [i for i,c in enumerate(chars) if c.lower() in HOMOGLYPH_MAP]
    n_replace = max(1, int(len(eligible)*rate))
    for idx in rng.sample(eligible, min(n_replace, len(eligible))):
        chars[idx] = HOMOGLYPH_MAP[chars[idx].lower()]
    return ''.join(chars)

def apply_synonym(text, rate, rng=None):
    if rng is None: rng = random.Random(SEED)
    words = text.split()
    n_replace = max(1, int(len(words)*rate))
    for idx in rng.sample(range(len(words)), min(n_replace, len(words))):
        word = words[idx]
        for syn in wordnet.synsets(word):
            for lemma in syn.lemmas():
                candidate = lemma.name().replace('_',' ')
                if candidate.lower() != word.lower():
                    words[idx] = candidate; break
            else: continue
            break
    return ' '.join(words)

def perturb(texts, attack_fn, rate):
    rng = random.Random(SEED)
    return [attack_fn(t, rate, rng) for t in texts]

# ── Bootstrap ─────────────────────────────────────────────────────────────────
def bootstrap_test(labels, preds, baseline_acc, n_boot=N_BOOTSTRAP):
    labels = np.array(labels); preds = np.array(preds)
    n = len(labels); rng = np.random.RandomState(SEED)
    boot_accs = []
    for _ in range(n_boot):
        idx = rng.randint(0, n, size=n)
        boot_accs.append(accuracy_score(labels[idx], preds[idx]))
    boot_accs = np.array(boot_accs)
    t_stat, p_val = stats.ttest_1samp(boot_accs, popmean=baseline_acc)
    return {"mean_acc": boot_accs.mean(), "std": boot_accs.std(),
            "ci_95_low": np.percentile(boot_accs, 2.5),
            "ci_95_high": np.percentile(boot_accs, 97.5),
            "t_stat": t_stat, "p_raw": p_val}


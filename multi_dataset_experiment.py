    for attack in ["Homoglyph","Synonym"]:
        for rate in PERTURBATION_RATES:
            row = df_ds[(df_ds["attack"]==attack)&(df_ds["rate"]==rate)]
            if row.empty: continue
            row   = row.iloc[0]
            s_row = df_s[(df_s["attack"]==attack)&(df_s["rate"]==rate)] \
                    if not df_s.empty else pd.DataFrame()

            ci_str = "—"; p_str = "—"
            if not s_row.empty:
                s = s_row.iloc[0]
                ci_str = f"[{s['ci_95_low']*100:.1f}%, {s['ci_95_high']*100:.1f}%]"
                p_str  = "<0.001" if s["p_corrected"]<0.001 \
                         else f"{s['p_corrected']:.4f}"

            summary_rows.append({
                "Dataset": ds, "Attack": attack,
                "Rate": f"{int(rate*100)}%",
                "Baseline": f"{base['accuracy']*100:.2f}%",
                "Accuracy": f"{row['accuracy']*100:.2f}%",
                "Precision": f"{row['precision']*100:.2f}%",
                "Recall": f"{row['recall']*100:.2f}%",
                "F1": f"{row['f1']*100:.2f}%",
                "95% CI": ci_str, "p-corr": p_str,
                "FP (base)": int(base["cm_fp"]),
                "FP (atk)":  int(row["cm_fp"]),
            })

